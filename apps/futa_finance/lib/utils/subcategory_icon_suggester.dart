  /// サブカテゴリ名から推測した絵文字を返す。マッチしなければ null。
  /// 大文字小文字は区別しない（英字キーワード用）。
  static String? suggestFor(String subName) {
    final lower = subName.toLowerCase();
    for (final rule in _rules) {
      final key = rule.$1.toLowerCase();
      if (lower.contains(key)) return rule.$2;
    }
    return null;
  }

  /// CategoryConfig 全体を走査し、subIcons が未設定のサブカテゴリに
  /// 推測アイコンを当てた新しい CategoryConfig を返す。
  /// 既に設定されているアイコンは絶対に上書きしない。
  ///
  /// 戻り値の int は「新規にアイコンが付与されたサブカテゴリの数」。
  static (CategoryConfig, int) applyToConfig(CategoryConfig config) {
    int applied = 0;
    final newMajors = config.majors.map((m) {
      final newIcons = <String, String>{
        ...?m.subIcons,
      };
      for (final sub in m.subs) {
        if (newIcons[sub] != null && newIcons[sub]!.isNotEmpty) continue;
        final guess = suggestFor(sub);
        if (guess != null) {
          newIcons[sub] = guess;
          applied++;
        }
      }
      return m.copyWith(subIcons: newIcons);
    }).toList();
    return (CategoryConfig(majors: newMajors), applied);
  }
