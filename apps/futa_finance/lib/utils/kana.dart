/// 半角カナ→全角カナの正規化。
///
/// 銀行CSV（新生など）の摘要は半角カナ（ｶﾅ）で入ってくる。半角の濁点(ﾞ)/半濁点(ﾟ)は
/// フォントによっては字形が無く「□」になって文字化けして見えるため、取り込み時に
/// 全角カナへ正規化して見やすく・文字化けしないようにする。
library;

const Map<String, String> _base = {
  'ｱ': 'ア', 'ｲ': 'イ', 'ｳ': 'ウ', 'ｴ': 'エ', 'ｵ': 'オ',
  'ｶ': 'カ', 'ｷ': 'キ', 'ｸ': 'ク', 'ｹ': 'ケ', 'ｺ': 'コ',
  'ｻ': 'サ', 'ｼ': 'シ', 'ｽ': 'ス', 'ｾ': 'セ', 'ｿ': 'ソ',
  'ﾀ': 'タ', 'ﾁ': 'チ', 'ﾂ': 'ツ', 'ﾃ': 'テ', 'ﾄ': 'ト',
  'ﾅ': 'ナ', 'ﾆ': 'ニ', 'ﾇ': 'ヌ', 'ﾈ': 'ネ', 'ﾉ': 'ノ',
  'ﾊ': 'ハ', 'ﾋ': 'ヒ', 'ﾌ': 'フ', 'ﾍ': 'ヘ', 'ﾎ': 'ホ',
  'ﾏ': 'マ', 'ﾐ': 'ミ', 'ﾑ': 'ム', 'ﾒ': 'メ', 'ﾓ': 'モ',
  'ﾔ': 'ヤ', 'ﾕ': 'ユ', 'ﾖ': 'ヨ',
  'ﾗ': 'ラ', 'ﾘ': 'リ', 'ﾙ': 'ル', 'ﾚ': 'レ', 'ﾛ': 'ロ',
  'ﾜ': 'ワ', 'ｦ': 'ヲ', 'ﾝ': 'ン',
  'ｧ': 'ァ', 'ｨ': 'ィ', 'ｩ': 'ゥ', 'ｪ': 'ェ', 'ｫ': 'ォ',
  'ｬ': 'ャ', 'ｭ': 'ュ', 'ｮ': 'ョ', 'ｯ': 'ッ',
  'ｰ': 'ー', '｡': '。', '｢': '「', '｣': '」', '､': '、', '･': '・',
};

const Map<String, String> _voiced = {
  'カ': 'ガ', 'キ': 'ギ', 'ク': 'グ', 'ケ': 'ゲ', 'コ': 'ゴ',
  'サ': 'ザ', 'シ': 'ジ', 'ス': 'ズ', 'セ': 'ゼ', 'ソ': 'ゾ',
  'タ': 'ダ', 'チ': 'ヂ', 'ツ': 'ヅ', 'テ': 'デ', 'ト': 'ド',
  'ハ': 'バ', 'ヒ': 'ビ', 'フ': 'ブ', 'ヘ': 'ベ', 'ホ': 'ボ',
  'ウ': 'ヴ',
};

const Map<String, String> _semiVoiced = {
  'ハ': 'パ', 'ヒ': 'ピ', 'フ': 'プ', 'ヘ': 'ペ', 'ホ': 'ポ',
};

/// 半角カナを全角カナへ変換する（濁点・半濁点は前の字と合成）。
/// カナ以外の文字（英数字・記号・漢字）はそのまま残す。
String halfToFullKana(String input) {
  if (input.isEmpty) return input;
  final chars = input.split('');
  final buf = StringBuffer();
  for (var i = 0; i < chars.length; i++) {
    final c = chars[i];
    var full = _base[c] ?? c;
    if (i + 1 < chars.length) {
      final next = chars[i + 1];
      if (next == 'ﾞ' && _voiced.containsKey(full)) {
        full = _voiced[full]!;
        i++;
      } else if (next == 'ﾟ' && _semiVoiced.containsKey(full)) {
        full = _semiVoiced[full]!;
        i++;
      }
    }
    // 単独で残った半角の濁点/半濁点は全角へ。
    if (full == 'ﾞ') full = '゛';
    if (full == 'ﾟ') full = '゜';
    buf.write(full);
  }
  return buf.toString();
}
